#include<stdio.h>

int main(void)
{

	int num1 = 10;
	printf("num1 << 1= %d\n",num1 << 1);
	printf("num1 << 3= %d\n",num1 << 3);
	printf("num1 >> 2= %d\n",num1 >> 2);
	printf("num1 & 3= %d\n",num1 & 3);
	printf("num1 | 16= %d\n",num1 | 16);
	printf("num1 ^ 5= %d\n",num1 ^ 5);
	printf("num1 ^ 3= %d\n",num1 ^ 3);
	printf("~num1 = %d\n",~num1);
	return 0;
	

}
